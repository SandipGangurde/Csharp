namespace IntroductionToCsharp
{
    internal class Program
    {
        static void Main()
        {
            // Charcter literals
            Console.WriteLine('P');
            // string literals
            Console.WriteLine("Pahkhs");

            // Integer literals
            Console.WriteLine(21345);

            // Floating point literals
            /*
                1. Float = F
                2. Double 
                3. Decimal = m
            */
            Console.WriteLine(0.23F);

            // Boolean literal
            // true or false
            Console.WriteLine(true);
            Console.WriteLine(false);
        }
    }
}